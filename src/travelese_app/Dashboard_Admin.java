    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    String namaPelanggan = txtNamaPelanggan.getText().trim();
        String telepon = txtTelepon.getText().trim();
        int idPaket = getSelectedIdPaket();
        int jumlahPeserta;
        double totalHarga;
        String status = "";
        java.sql.Date tanggalPesan = new java.sql.Date(System.currentTimeMillis());

        try {
            jumlahPeserta = Integer.parseInt(txtJumlahPeserta.getText().trim());
            totalHarga = ambilHargaPaket(idPaket) * jumlahPeserta;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah peserta harus angka!");
            return;
        }

        if (rbLunas.isSelected()) status = "LUNAS";
        else if (rbBelumBayar.isSelected()) status = "BELUM BAYAR";
        else if (rbBatal.isSelected()) status = "BATAL";
        else {
            JOptionPane.showMessageDialog(this, "Pilih status pembayaran!");
            return;
        }

        if (namaPelanggan.isEmpty() || telepon.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama pelanggan & telepon wajib diisi!");
            return;
        }

        try {
            Connection conn = Database.getConnection();
            String sql = "INSERT INTO dashboard_admin "
                       + "(nama_pelanggan, telepon, id_paket, jumlah_peserta, total_harga, status, tanggal_pesan) "
                       + "VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, namaPelanggan);
            pst.setString(2, telepon);
            pst.setInt(3, idPaket);
            pst.setInt(4, jumlahPeserta);
            pst.setDouble(5, totalHarga);
            pst.setString(6, status);
            pst.setDate(7, tanggalPesan);

            int hasil = pst.executeUpdate();
            if (hasil > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
                resetForm();
                loadDataTransaksi();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal disimpan!");
            }
            pst.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal simpan data: " + ex.getMessage());
        }

    }//GEN-LAST:event_btnSimpanActionPerformed

