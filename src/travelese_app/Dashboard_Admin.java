    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    int selectedRow = tblDashboard.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus!");
            return;
        }
        int id = (int) tblDashboard.getValueAt(selectedRow, 0); // kolom pertama = id_pemesanan
        int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin hapus data ini?");
        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                Connection conn = Database.getConnection();
                String sql = "DELETE FROM dashboard_admin WHERE id_pemesanan=?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setInt(1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                loadDataTransaksi();
                resetForm();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Gagal hapus data: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

