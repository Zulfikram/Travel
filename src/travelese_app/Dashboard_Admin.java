    private void loadPaketWisata() {
        cmbPaketWisata.removeAllItems();
        try {
            Connection conn = Database.getConnection();
            String sql = "SELECT id_paket, nama_paket FROM paket_wisata";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                cmbPaketWisata.addItem(rs.getInt("id_paket") + " - " + rs.getString("nama_paket"));
            }
            rs.close();
            pst.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal load paket wisata: " + ex.getMessage());
        }
    }


    private void loadDataTransaksi() {
        DefaultTableModel model = (DefaultTableModel) tblDashboard.getModel();
        model.setRowCount(0);
        try {
            Connection conn = Database.getConnection();
            String sql = "SELECT d.id_pemesanan, d.nama_pelanggan, d.telepon, p.nama_paket, d.jumlah_peserta, d.total_harga, d.status, d.tanggal_pesan, d.id_paket "
                        + "FROM dashboard_admin d JOIN paket_wisata p ON d.id_paket = p.id_paket";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                Object[] row = {
                    rs.getInt("id_pemesanan"),
                    rs.getString("nama_pelanggan"),
                    rs.getString("telepon"),
                    rs.getString("nama_paket"),
                    rs.getInt("jumlah_peserta"),
                    rs.getDouble("total_harga"),
                    rs.getString("status"),
                    rs.getDate("tanggal_pesan"),
                    rs.getInt("id_paket")
                };
                model.addRow(row);
            }
            rs.close();
            pst.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal load data: " + ex.getMessage());
        }
    }
    private void tblDashboardMouseClicked(java.awt.event.MouseEvent evt) {                                          
        int row = tblDashboard.getSelectedRow();
        if (row != -1) {
            txtNamaPelanggan.setText(tblDashboard.getValueAt(row, 1).toString());
            txtTelepon.setText(tblDashboard.getValueAt(row, 2).toString());
            // Cari index paket yang sesuai
            int idPaket = Integer.parseInt(tblDashboard.getValueAt(row, 8).toString());
            for (int i = 0; i < cmbPaketWisata.getItemCount(); i++) {
                if (cmbPaketWisata.getItemAt(i).startsWith(idPaket + " -")) {
                    cmbPaketWisata.setSelectedIndex(i);
                    break;
                }
            }
            txtJumlahPeserta.setText(tblDashboard.getValueAt(row, 4).toString());
            String status = tblDashboard.getValueAt(row, 6).toString();
            if (status.equals("LUNAS")) rbLunas.setSelected(true);
            else if (status.equals("BELUM BAYAR")) rbBelumBayar.setSelected(true);
            else if (status.equals("BATAL")) rbBatal.setSelected(true);
            // Tampilkan total harga
            lblTotalHarga.setText("Rp " + tblDashboard.getValueAt(row, 5).toString());
        }
    }

