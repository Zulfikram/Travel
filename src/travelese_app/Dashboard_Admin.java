    private double ambilHargaPaket(int idPaket) {
        double harga = 0;
        try {
            Connection conn = Database.getConnection();
            String sql = "SELECT harga FROM paket_wisata WHERE id_paket=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, idPaket);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                harga = rs.getDouble("harga");
            }
            rs.close();
            pst.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal mengambil harga paket: " + ex.getMessage());
        }
        return harga;
    }
    private int getSelectedIdPaket() {
            String selected = (String) cmbPaketWisata.getSelectedItem();
            if (selected != null && selected.contains(" - ")) {
                return Integer.parseInt(selected.split(" - ")[0]);
            }
            return 0;
        }
    private void hitungTotalHarga() {
        int idPaket = getSelectedIdPaket();
        double hargaPaket = ambilHargaPaket(idPaket);
        int jumlah = 0;
        try {
            jumlah = Integer.parseInt(txtJumlahPeserta.getText());
        } catch (NumberFormatException e) {}
        double total = hargaPaket * jumlah;
        // Tampilkan di field jika mau, contoh:
        lblTotalHarga.setText("Rp " + total); // pastikan ada JLabel lblTotalHarga di desain
    }
    
