    private void setComboBoxValues() {
        cmbLokasi.removeAllItems();
        cmbHarga.removeAllItems();
        cmbLokasi.addItem("Semua");

        cmbHarga.addItem("Semua");
        cmbHarga.addItem("Murah");
        cmbHarga.addItem("Mahal");
        
        try {
            Connection conn = Database.getConnection();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT DISTINCT lokasi FROM paket_wisata");
            while (rs.next()) {
                cmbLokasi.addItem(rs.getString("lokasi"));
            }
            rs.close();
            st.close();
            conn.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Gagal memuat lokasi: " + ex.getMessage());
        }
    }
   
    

