    private void loadData() {
        String lokasiFilter = cmbLokasi.getSelectedItem().toString();
        String hargaFilter = cmbHarga.getSelectedItem().toString();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama Paket");
        model.addColumn("Lokasi");
        model.addColumn("Harga");
        model.addColumn("Fasilitas");

        try {
            Connection conn = Database.getConnection();
            String sql = "SELECT * FROM paket_wisata WHERE 1=1";

            if (!lokasiFilter.equals("Semua")) {
                sql += " AND lokasi = ?";
            }
            
if (hargaFilter != null) {
    if (hargaFilter.equals("Murah")) {
        sql += " AND harga < 1000000";
    } else if (hargaFilter.equals("Sedang")) {
        sql += " AND harga >= 1000000 AND harga < 2000000";
    } else if (hargaFilter.equals("Mahal")) {
        sql += " AND harga >= 2000000";
    }
}


            PreparedStatement pst = conn.prepareStatement(sql);

            if (!lokasiFilter.equals("Semua")) {
                pst.setString(1, lokasiFilter);
            }

            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("nama_paket"),
                    rs.getString("lokasi"),
                    rs.getDouble("harga"),
                    rs.getString("fasilitas")
                });
            }

            tblWisata.setModel(model);

            rs.close();
            pst.close();
            conn.close();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Gagal mengambil data: " + ex.getMessage());
        }
    }

    private void lihatDetail() {
        int row = tblWisata.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih data terlebih dahulu.");
            return;
        }

        String nama = tblWisata.getValueAt(row, 0).toString();
        String lokasi = tblWisata.getValueAt(row, 1).toString();
        String harga = tblWisata.getValueAt(row, 2).toString();
        String fasilitas = tblWisata.getValueAt(row, 3).toString();

        JOptionPane.showMessageDialog(this,
            "Paket: " + nama + "\n" +
            "Lokasi: " + lokasi + "\n" +
            "Harga: Rp " + harga + "\n" +
            "Fasilitas: " + fasilitas,
            "Detail Paket",
            JOptionPane.INFORMATION_MESSAGE
        );
    }
